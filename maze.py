    Tile_Size = 50

    
    WIDTH = len(LEVEL[0]*Tile_Size)
    HEIGHT = len(LEVEL*Tile_Size)

    def draw_map(self, screen, level):
        global Tile_Size, blue
        for y, row in enumerate(level):
            for x, tile in enumerate(row):
                if tile == "#":
                    rect = pygame.Rect(
                        x * self.Tile_Size,
                        y * self.Tile_Size,
                        self.Tile_Size,
                        self.Tile_Size
                    )
                    pygame.draw.rect(screen, self.blue, rect)